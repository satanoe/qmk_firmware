// Based on the code from https://gist.github.com/okapies/5d13a174cbb13ce34dbd9faede9d0b71#file-keymap-c-L99-L164
static bool lower_pressed = false;
static uint16_t lower_pressed_time = 0;
static bool raise_pressed = false;
static uint16_t raise_pressed_time = 0;
static bool sp_num_pressed = false;
static uint16_t sp_num_pressed_time = 0;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case LOWER:
            if (record->event.pressed) {
                lower_pressed      = true;
                lower_pressed_time = record->event.time;
                layer_on(_LOWER);
            } else {
                layer_off(_LOWER);
                if (lower_pressed && (TIMER_DIFF_16(record->event.time, lower_pressed_time) < TAPPING_TERM)) {
                    register_code(KC_LANG2);  // for macOS
                    register_code(KC_MHEN);
                    unregister_code(KC_MHEN);
                    unregister_code(KC_LANG2);
                }
                lower_pressed = false;
            }
            return false;
            break;
        case RAISE:
            if (record->event.pressed) {
                raise_pressed      = true;
                raise_pressed_time = record->event.time;
                layer_on(_RAISE);
            } else {
                layer_off(_RAISE);
                if (raise_pressed && (TIMER_DIFF_16(record->event.time, raise_pressed_time) < TAPPING_TERM)) {
                    register_code(KC_LANG1);  // for macOS
                    register_code(KC_HENK);
                    unregister_code(KC_HENK);
                    unregister_code(KC_LANG1);
                }
                raise_pressed = false;
            }
            return false;
            break;
        case SP_NUM:
            if (record->event.pressed) {
                sp_num_pressed      = true;
                sp_num_pressed_time = record->event.time;
                layer_on(_NUMPAD);
            } else {
                layer_off(_NUMPAD);
                if (sp_num_pressed && (TIMER_DIFF_16(record->event.time, sp_num_pressed_time) < TAPPING_TERM)) {
                    register_code(KC_SPC);  // for macOS
                    unregister_code(KC_SPC);
                }
                sp_num_pressed = false;
            }
            return false;
            break;
        default:
            if (record->event.pressed) {
                // reset the flags
                lower_pressed = false;
                raise_pressed = false;
                sp_num_pressed = false;
            }
            break;
    }
    return true;
}